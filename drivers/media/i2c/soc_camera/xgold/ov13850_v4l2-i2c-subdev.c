static int ov13850_g_timings(struct ov_camera_module *cam_mod,
	struct ov_camera_module_timings *timings)
{
	int ret = 0;
	u32 reg_val;
	u32 pll2_prediv0;
	u32 pll2_prediv_x2;
	u32 pll2_multiplier;
	u32 pll2_sys_prediv;
	u32 pll2_sys_div_x2;
	u32 win_off;

	if (IS_ERR_OR_NULL(cam_mod->active_config))
		goto err;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_PLL2_PREDIV_REG,
		&reg_val)))
		goto err;

	pll2_prediv0 = (reg_val >> 3) & 0x1;

	if (pll2_prediv0 == 0)
		pll2_prediv0 = 1;
	else
		pll2_prediv0 = 2;

	pll2_prediv_x2 = reg_val & 0x7;

	switch (pll2_prediv_x2)	{
	case 0:
		pll2_prediv_x2 = 2;
		break;
	case 1:
		pll2_prediv_x2 = 3;
		break;
	case 2:
		pll2_prediv_x2 = 4;
		break;
	case 3:
		pll2_prediv_x2 = 5;
		break;
	case 4:
		pll2_prediv_x2 = 6;
		break;
	case 5:
		pll2_prediv_x2 = 8;
		break;
	case 6:
		pll2_prediv_x2 = 12;
		break;
	case 7:
		pll2_prediv_x2 = 16;
		break;
	}

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_PLL2_MUL_HIGH_REG,
		&reg_val)))
		goto err;

	pll2_multiplier = (reg_val & 0x3) << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_PLL2_MUL_LOW_REG,
		&reg_val)))
		goto err;

	pll2_multiplier |= (reg_val & 0xFF);

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_PLL2_DIVSYS_REG,
		&reg_val)))
		goto err;

	pll2_sys_prediv = (reg_val & 0xF) + 1;
	pll2_sys_div_x2 = (reg_val >> 4) & 0x7;

	switch (pll2_sys_div_x2) {
	case 0:
		pll2_sys_div_x2 = 2;
		break;
	case 1:
		pll2_sys_div_x2 = 3;
		break;
	case 2:
		pll2_sys_div_x2 = 4;
		break;
	case 3:
		pll2_sys_div_x2 = 5;
		break;
	case 4:
		pll2_sys_div_x2 = 6;
		break;
	case 5:
		pll2_sys_div_x2 = 7;
		break;
	case 6:
		pll2_sys_div_x2 = 8;
		break;
	case 7:
		pll2_sys_div_x2 = 10;
		break;
	}

	timings->vt_pix_clk_freq_hz =
		((4 * OV13850_EXT_CLK) /
		(pll2_prediv0 * pll2_prediv_x2 *
		pll2_sys_prediv * pll2_sys_div_x2)) *
		pll2_multiplier;

	timings->vt_pix_clk_freq_hz *= 4;

	/*VTS*/
	ret = ov13850_g_VTS(cam_mod, &timings->frame_length_lines);
	if (IS_ERR_VALUE(ret))
		goto err;

	/*HTS*/
	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_TIMING_HTS_HIGH_REG,
		&reg_val)))
		goto err;

	timings->line_length_pck = reg_val << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_TIMING_HTS_LOW_REG,
		&reg_val)))
		goto err;

	timings->line_length_pck |= reg_val;

	timings->coarse_integration_time_min = 1;
	timings->coarse_integration_time_max_margin =
		OV13850_INTEGRATION_TIME_MARGIN;

	/* OV Sensor do not use fine integration time. */
	timings->fine_integration_time_min = 0;
	timings->fine_integration_time_max_margin = 0;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_TIMING_X_INC,
		&reg_val)))
		goto err;

	timings->binning_factor_x = ((reg_val >> 4) + 1) / 2;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_TIMING_Y_INC,
		&reg_val)))
		goto err;

	timings->binning_factor_y = ((reg_val >> 4) + 1) / 2;

	/* Get the cropping and output resolution to ISP for this mode. */
	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_HORIZONTAL_START_HIGH_REG,
		&reg_val)))
		goto err;

	timings->crop_horizontal_start = reg_val << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_HORIZONTAL_START_LOW_REG,
		&reg_val)))
		goto err;

	timings->crop_horizontal_start |= reg_val;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_VERTICAL_START_HIGH_REG,
		&reg_val)))
		goto err;

	timings->crop_vertical_start = reg_val << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_VERTICAL_START_LOW_REG,
		&reg_val)))
		goto err;

	timings->crop_vertical_start |= reg_val;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_HORIZONTAL_END_HIGH_REG,
		&reg_val)))
		goto err;

	timings->crop_horizontal_end = reg_val << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_HORIZONTAL_END_LOW_REG,
		&reg_val)))
		goto err;

	timings->crop_horizontal_end |= reg_val;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_VERTICAL_END_HIGH_REG,
		&reg_val)))
		goto err;

	timings->crop_vertical_end = reg_val << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_VERTICAL_END_LOW_REG,
		&reg_val)))
		goto err;

	timings->crop_vertical_end |= reg_val;

	/* The sensor can do windowing within the cropped array.
	Take this into the cropping size reported. */
	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_H_WIN_OFF_HIGH_REG,
		&reg_val)))
		goto err;

	win_off = (reg_val & 0xf) << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_H_WIN_OFF_LOW_REG,
		&reg_val)))
		goto err;

	win_off |= (reg_val & 0xff);

	timings->crop_horizontal_start += win_off;
	timings->crop_horizontal_end -= win_off;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_V_WIN_OFF_HIGH_REG,
		&reg_val)))
		goto err;

	win_off = (reg_val & 0xf) << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_V_WIN_OFF_LOW_REG,
		&reg_val)))
		goto err;

	win_off |= (reg_val & 0xff);

	timings->crop_vertical_start += win_off;
	timings->crop_vertical_end -= win_off;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_HORIZONTAL_OUTPUT_SIZE_HIGH_REG,
		&reg_val)))
		goto err;

	timings->sensor_output_width = reg_val << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_HORIZONTAL_OUTPUT_SIZE_LOW_REG,
		&reg_val)))
		goto err;

	timings->sensor_output_width |= reg_val;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_VERTICAL_OUTPUT_SIZE_HIGH_REG,
		&reg_val)))
		goto err;

	timings->sensor_output_height = reg_val << 8;

	if (IS_ERR_VALUE(ov_camera_module_read_reg_table(
		cam_mod,
		OV13850_VERTICAL_OUTPUT_SIZE_LOW_REG,
		&reg_val)))
		goto err;

	timings->sensor_output_height |= reg_val;

	return ret;
err:
	ov_camera_module_pr_err(cam_mod,
			"failed with error (%d)\n", ret);
	return ret;
}

