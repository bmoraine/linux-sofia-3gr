			memcpy(pPara + 52, pMiddle + 14, 1);
			memcpy(pPara + 53, pCenter + 4, 4);
			memcpy(pPara + 57, pMiddle + 15, 1);
			memcpy(pPara + 58, pCorner + 34, 4);
			memcpy(pPara + 62, pMiddle + 16, 1);
			memcpy(pPara + 63, pCenter + 8, 4);
			memcpy(pPara + 67, pMiddle + 17, 1);
