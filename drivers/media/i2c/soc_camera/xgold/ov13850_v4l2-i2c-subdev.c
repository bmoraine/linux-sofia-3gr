/* return value:
   bit[7]: 0 no otp info, 1 valid otp info
   bit[6]: 0 no otp wb, 1 valib otp wb
   bit[5]: 0 no otp vcm, 1 valid otp vcm
   bit[4]: 0 no otp lenc, 1 valid otp lenc */
int ov13850_read_otp(struct ov_camera_module *cam_mod)
{
	int otp_flag, addr, temp, i;
	int checksumLSC = 0, checksumOTP = 0, checksumTotal = 0;
	uint8_t lenc_out[360] = {0};
	int ret = 0;

	if (otp_ptr != NULL) {
		ov_camera_module_pr_debug(cam_mod, "OTP data loaded already\n");
		return 0;
	} else {
		otp_ptr = kzalloc(sizeof(*otp_ptr), GFP_KERNEL);
		if (!otp_ptr) {
			ov_camera_module_pr_err(cam_mod, "otp alloc fail!\n");
			return -ENOMEM;
		}
	}

	ret = ov_camera_module_write_reg(cam_mod, 0x0100, 1);

	/*set 0x5002[1] to "0" */
	ret |= ov_camera_module_read_reg(cam_mod, 1, 0x5002, &temp);
	ret |= ov_camera_module_write_reg(cam_mod, 0x5002, (temp & (~0x02)));

	/* read OTP into buffer */
	ret |= ov_camera_module_write_reg(cam_mod, 0x3d84, 0xC0);

	/* OTP start address */
	ret |= ov_camera_module_write_reg(cam_mod, 0x3d88, 0x72);
	ret |= ov_camera_module_write_reg(cam_mod, 0x3d89, 0x20);

	/* OTP end address */
	ret |= ov_camera_module_write_reg(cam_mod, 0x3d8A, 0x73);
	ret |= ov_camera_module_write_reg(cam_mod, 0x3d8B, 0xBE);

	/* load otp into buffer */
	ret |= ov_camera_module_write_reg(cam_mod, 0x3d81, 0x01);

	usleep_range(10000, 15000);

	/* OTP base information and WB calibration data */
	ret |= ov_camera_module_read_reg(cam_mod, 1, 0x7220, &otp_flag);

	addr = temp = 0;
	if ((otp_flag & 0xc0) == 0x40)
		addr = 0x7221; /* base address of info group 1 */
	else if ((otp_flag & 0x30) == 0x10)
		addr = 0x7229; /* base address of info group 2 */

	if (addr != 0) {
		otp_ptr->flag = 0xC0; /* valid info and AWB in OTP */
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr,
				&(otp_ptr->module_integrator_id));
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 1,
				&(otp_ptr->lens_id));
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 2,
				&(otp_ptr->production_year));
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 3,
				&(otp_ptr->production_month));
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 4,
				&(otp_ptr->production_day));
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 7, &temp);

		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 5,
				&(otp_ptr->rg_ratio));
		otp_ptr->rg_ratio = (otp_ptr->rg_ratio << 2) +
				((temp >> 6) & 0x03);

		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 6,
				&(otp_ptr->bg_ratio));
		otp_ptr->bg_ratio = (otp_ptr->bg_ratio << 2) +
				((temp >> 4) & 0x03);
	} else {
		otp_ptr->flag = 0x00; /* not info in OTP */
		otp_ptr->module_integrator_id = 0;
		otp_ptr->lens_id = 0;
		otp_ptr->production_year = 0;
		otp_ptr->production_month = 0;
		otp_ptr->production_day = 0;
	}

	/* OTP VCM Calibration */
	ret |= ov_camera_module_read_reg(cam_mod, 1, 0x73ac, &otp_flag);
	addr = temp = 0;
	if ((otp_flag & 0xc0) == 0x40) {
		/* base address of VCM Calibration group 1 */
		addr = 0x73ad;
	} else if ((otp_flag & 0x30) == 0x10) {
		/* base address of VCM Calibration group 2 */
		addr = 0x73b0;
	}

	if (addr != 0) {
		otp_ptr->flag |= 0x20;
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 2, &temp);

		ret |= ov_camera_module_read_reg(cam_mod, 1, addr,
				&(otp_ptr->VCM_start));
		otp_ptr->VCM_start = (otp_ptr->VCM_start << 2) |
							((temp >> 6) & 0x03);

		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 1,
				&(otp_ptr->VCM_end));
		otp_ptr->VCM_end = (otp_ptr->VCM_end << 2) |
							((temp >> 4) & 0x03);

		otp_ptr->VCM_dir = (temp >> 2) & 0x03;
	} else {
		otp_ptr->VCM_start = 0;
		otp_ptr->VCM_end = 0;
		otp_ptr->VCM_dir = 0;
	}

	/* OTP Lenc Calibration */
	ov_camera_module_read_reg(cam_mod, 1, 0x7231, &otp_flag);
	addr = 0;

	if ((otp_flag & 0xc0) == 0x40) {
		/* base address of Lenc Calibration group 1 */
		addr = 0x7232;
	} else if ((otp_flag & 0x30) == 0x10) {
		/* base address of Lenc Calibration group 2 */
		addr = 0x72ef;
	}

	if (addr != 0) {
		for (i = 0; i < 186; i++) {
			temp = 0;
			ret |= ov_camera_module_read_reg(cam_mod, 1,
						addr + i, &temp);
			otp_ptr->lenc[i] = (uint8_t) temp;
			checksumLSC += temp;
		}

		/* Decode the lenc buffer from OTP ,
		*from 186 bytes to 360 bytes */
		ov13850_r2a_lenc_decoder(otp_ptr->lenc, lenc_out);

		for (i = 0; i < 360; i++)
			checksumOTP += lenc_out[i];

		checksumLSC = (checksumLSC) % 255 + 1;
		checksumOTP = (checksumOTP) % 255 + 1;
		checksumTotal = (checksumLSC) ^ (checksumOTP);
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 186,
				&(otp_ptr->checksumLSC));
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 187,
				&(otp_ptr->checksumOTP));
		ret |= ov_camera_module_read_reg(cam_mod, 1, addr + 188,
				&(otp_ptr->checksumTotal));

		if (otp_ptr->checksumLSC == checksumLSC
				&& otp_ptr->checksumOTP == checksumOTP) {
			otp_ptr->flag |= 0x10;
		} else if (otp_ptr->checksumTotal == checksumTotal) {
			otp_ptr->flag |= 0x10;
		}

	} else {
		for (i = 0; i < 186; i++)
			otp_ptr->lenc[i] = 0;
	}

	for (i = 0x7220; i <= 0x73be; i++) {
		/* clear OTP buffer and recommended
		use continuous write to accelarate */
		ret |= ov_camera_module_write_reg(cam_mod, i, 0);
	}

	/* set 0x5002[1] to "1" */
	temp = 0;
	ret |= ov_camera_module_read_reg(cam_mod, 1, 0x5002, &temp);
	ret |= ov_camera_module_write_reg(cam_mod, 0x5002,
			(0x02 | (temp & (~0x02))));

	ret |= ov_camera_module_write_reg(cam_mod, 0x0100, 0);

	ov13850_print_otp_info(cam_mod);

	return ret;
}

/* return value:
   bit[7]: 0 no otp info, 1 valid otp info
   bit[6]: 0 no otp wb, 1 valib otp wb
   bit[5]: 0 no otp vcm, 1 valid otp vcm
   bit[4]: 0 no otp lenc, 1 valid otp lenc */
void ov13850_apply_otp(struct ov_camera_module *cam_mod)
{
	int rg, bg;
	int Base_gain, temp, i;
	uint8_t lenc_out[360] = {0};

	if (otp_ptr == NULL)
		return;

	/* apply OTP WB Calibration */
	if (otp_ptr->flag & 0x40) {
		rg = otp_ptr->rg_ratio;
		bg = otp_ptr->bg_ratio;

		/*calculate G gain */
		otp_ptr->R_gain = (RG_Ratio_Typical * 1000) / rg;
		otp_ptr->B_gain = (BG_Ratio_Typical * 1000) / bg;
		otp_ptr->G_gain = 1000;
		if (otp_ptr->R_gain < 1000 || otp_ptr->B_gain < 1000) {
			if (otp_ptr->R_gain < otp_ptr->B_gain)
				Base_gain = otp_ptr->R_gain;
			else
				Base_gain = otp_ptr->B_gain;
		} else {
			Base_gain = otp_ptr->G_gain;
		}

		otp_ptr->R_gain = 0x400 * otp_ptr->R_gain / (Base_gain);
		otp_ptr->B_gain = 0x400 * otp_ptr->B_gain / (Base_gain);
		otp_ptr->G_gain = 0x400 * otp_ptr->G_gain / (Base_gain);
		/* update sensor WB gain */
		if (otp_ptr->R_gain > 0x400) {
			ov_camera_module_write_reg(cam_mod, 0x5056,
					otp_ptr->R_gain >> 8);
			ov_camera_module_write_reg(cam_mod, 0x5057,
					otp_ptr->R_gain & 0x00ff);
		}
		if (otp_ptr->G_gain > 0x400) {
			ov_camera_module_write_reg(cam_mod, 0x5058,
					otp_ptr->G_gain >> 8);
			ov_camera_module_write_reg(cam_mod, 0x5059,
					otp_ptr->G_gain & 0x00ff);
		}
		if (otp_ptr->B_gain > 0x400) {
			ov_camera_module_write_reg(cam_mod, 0x505A,
					otp_ptr->B_gain >> 8);
			ov_camera_module_write_reg(cam_mod, 0x505B,
					otp_ptr->B_gain & 0x00ff);
		}
	}

	/* apply OTP Lenc Calibration */
	if (otp_ptr->flag & 0x10) {
		ov_camera_module_read_reg(cam_mod, 1, 0x5000, &temp);
		temp = 0x01 | temp;
		ov_camera_module_write_reg(cam_mod, 0x5000, temp);

		/* Decode the lenc buffer from OTP ,
		* from 186 bytes to 360 bytes */
		ov13850_r2a_lenc_decoder(otp_ptr->lenc, lenc_out);

		for (i = 0; i < 360; i++)
			ov_camera_module_write_reg(cam_mod,
					0x5200 + i,
					lenc_out[i]);
	}
}

