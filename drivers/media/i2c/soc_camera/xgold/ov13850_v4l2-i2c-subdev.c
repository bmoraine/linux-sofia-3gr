static int ov13850_start_streaming(struct ov_camera_module *cam_mod)
{
	int ret = 0;

	ov_camera_module_pr_debug(cam_mod, "\n");

#ifdef OV13850_OTP_SUPPORT
	ov13850_apply_otp(cam_mod);
#endif

	ret = ov13850_g_VTS(cam_mod, &cam_mod->vts_min);
	if (IS_ERR_VALUE(ret))
		goto err;

	ov_camera_module_g_min_vts_lines(cam_mod,
		&cam_mod->min_lines_in_vbl);
	ov_camera_module_pr_debug(cam_mod,
		"minimum lines during VBL is: %d\n",
		cam_mod->min_lines_in_vbl);

	ret = ov13850_write_aec(cam_mod);
	if (IS_ERR_VALUE(ret))
		goto err;
	ret = ov13850_flip(cam_mod);
	if (IS_ERR_VALUE(ret))
		goto err;
	if (IS_ERR_VALUE(ov_camera_module_write_reg(cam_mod, 0x0100, 1)))
		goto err;

#ifdef DUMP_OTP
	ov13850_dump_otp(cam_mod);
#endif

	return 0;
err:
	ov_camera_module_pr_err(cam_mod, "failed with error (%d)\n",
		ret);
	return ret;
}

