void ov13850_color_decoder(uint8_t *pData, uint8_t *pPara)
{
	uint32_t Offset, Bit, Option;
	uint32_t i, k;
	uint8_t pBase[30];
	Offset = pData[0];
	Bit = pData[1] >> 7;
	Option = (pData[1] & 0x40) >> 6;
	/* 5~1 (byte1) */
	pPara[0] = (pData[1] & 0x3e) >> 1;
	/* 0 (byte1) and 7~4 (byte2) */
	pPara[1] = ((pData[1] & 0x1) << 4) |
			(pData[2] >> 4);
	/* 3~0 (byte2) and 7 (byte3) */
	pPara[2] = ((pData[2] & 0xf) << 1) |
			(pData[3] >> 7);
	/* 6~2 (byte3) */
	pPara[3] = (pData[3] & 0x7c) >> 2;
	/* 1~0 (byte3) and 7~5 (byte4) */
	pPara[4] = ((pData[3] & 0x3) << 3) |
			(pData[4] >> 5);
	/* 4~0 (byte4) */
	pPara[5] = pData[4] & 0x1f;

	for (i = 6, k = 5; i < 30; i += 8, k += 5) {
		/* 7~3 (byte0) */
		pPara[i] = pData[k] >> 3;
		/* 2~0 (byte0) and 7~6 (byte1) */
		pPara[i + 1] = ((pData[k] & 0x7) << 2) |
				(pData[k + 1] >> 6);
		/* 5~1 (byte1) */
		pPara[i + 2] = (pData[k + 1] & 0x3e) >> 1;
		/* 0 (byte1) and 7~4 (byte2) */
		pPara[i + 3] = ((pData[k + 1] & 0x1) << 4) |
				(pData[k + 2] >> 4);
		/* 3~0 (byte2) and 7 (byte3) */
		pPara[i + 4] = ((pData[k + 2] & 0xf) << 1) |
				(pData[k + 3] >> 7);
		/* 6~2 (byte3) */
		pPara[i + 5] = (pData[k + 3] & 0x7c) >> 2;
		/* 1~0 (byte3) and 7~5 (byte4) */
		pPara[i + 6] = ((pData[k + 3] & 0x3) << 3) |
				(pData[k + 4] >> 5);
		/* 4~0 (byte4) */
		pPara[i + 7] = pData[k + 4] & 0x1f;
	}

	memcpy(pBase, pPara, 30);
	for (i = 0, k = 20; i < 120; i += 4, k++) {
		pPara[i] = pData[k] >> 6;
		pPara[i + 1] = (pData[k] & 0x30) >> 4;
		pPara[i + 2] = (pData[k] & 0xc) >> 2;
		pPara[i + 3] = pData[k] & 0x3;
	}

	if (Option == 0) {/* 10x12 */
		for (i = 0; i < 5; i++) {
			for (k = 0; k < 6; k++) {
				pPara[i * 24 + k * 2] += pBase[i * 6 + k];
				pPara[i * 24 + k * 2 + 1] += pBase[i * 6 + k];
				pPara[i * 24 + k * 2 + 12] += pBase[i * 6 + k];
				pPara[i * 24 + k * 2 + 13] += pBase[i * 6 + k];
			}
		}
	} else {
		for (i = 0; i < 6; i++) {/* 12x10 */
			for (k = 0; k < 5; k++) {
				pPara[i * 20 + k * 2] += pBase[i * 5 + k];
				pPara[i * 20 + k * 2 + 1] += pBase[i * 5 + k];
				pPara[i * 20 + k * 2 + 10] += pBase[i * 5 + k];
				pPara[i * 20 + k * 2 + 11] += pBase[i * 5 + k];
			}
		}
	}

	for (i = 0; i < 120; i++)
		pPara[i] = (pPara[i] << Bit) + Offset;
}

