static int ov5670_check_camera_id(struct ov_camera_module *cam_mod)
{
	u32 pidh, pidl, vid = 0;
	int ret = 0;

	ov_camera_module_pr_info(cam_mod, "\n");

	ret |= ov_camera_module_read_reg(cam_mod, 1, OV5670_PIDH_ADDR, &pidh);
	ret |= ov_camera_module_read_reg(cam_mod, 1, OV5670_PIDL_ADDR, &pidl);

	if (IS_ERR_VALUE(ret)) {
		ov_camera_module_pr_err(cam_mod,
			"register read failed, camera module powered off?\n");
		goto err;
	}

	if ((pidh == OV5670_PIDH_MAGIC) && (pidl == OV5670_PIDL_MAGIC))
		ov_camera_module_pr_debug(cam_mod,
			"successfully detected camera ID 0x%02x%02x\n",
			pidh, pidl);
	else {
		ov_camera_module_pr_err(cam_mod,
			"wrong camera ID, expected 0x%02x%02x, detected 0x%02x%02x\n",
			OV5670_PIDH_MAGIC, OV5670_PIDL_MAGIC, pidh, pidl);
		ret = -EINVAL;
		goto err;
	}

	ret |= ov_camera_module_read_reg(cam_mod, 1, 0x302a, &vid);
	if (IS_ERR_VALUE(ret)) {
		ov_camera_module_pr_err(cam_mod,
			"register read failed, camera module powered off?\n");
		goto err;
	} else
		ov_camera_module_pr_debug(cam_mod,
			"OV5670 chip version is 0x%2x\n", vid);

	return 0;
err:
	ov_camera_module_pr_err(cam_mod, "failed with error (%d)\n", ret);
	return ret;
}

