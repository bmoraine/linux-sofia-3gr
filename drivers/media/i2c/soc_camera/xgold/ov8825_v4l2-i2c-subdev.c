static int ov8825_write_aec(struct ov_camera_module *cam_mod)
{
	int ret = 0;
	u32 exp_time = cam_mod->exp_config.exp_time;

	ov_camera_module_pr_debug(cam_mod,
		"exp_time = %d, gain = %d, flash_mode = %d\n",
		cam_mod->exp_config.exp_time,
		cam_mod->exp_config.gain,
		cam_mod->exp_config.flash_mode);

	/* if the sensor is already streaming, write to shadow registers,
		if the sensor is in SW standby, write to active registers,
		if the sensor is off/registers are not writeable, do nothing */
	if ((cam_mod->state == OV_CAMERA_MODULE_SW_STANDBY) ||
		(cam_mod->state == OV_CAMERA_MODULE_STREAMING)) {
		exp_time &= 0xfffffff0;
		if (cam_mod->state == OV_CAMERA_MODULE_STREAMING)
			ret = ov_camera_module_write_reg(cam_mod,
			OV8825_AEC_GROUP_UPDATE_ADDRESS,
			OV8825_AEC_GROUP_UPDATE_START_DATA);
		ret |= ov_camera_module_write_reg(cam_mod,
			OV8825_AEC_PK_LONG_GAIN_HIGH_REG,
			OV8825_FETCH_MSB_GAIN(cam_mod->exp_config.gain >> 4));
		ret |= ov_camera_module_write_reg(cam_mod,
			OV8825_AEC_PK_LONG_GAIN_LOW_REG,
			OV8825_FETCH_LSB_GAIN(cam_mod->exp_config.gain >> 4));
		ret = ov_camera_module_write_reg(cam_mod,
			OV8825_AEC_PK_LONG_EXPO_3RD_REG,
			OV8825_FETCH_3RD_BYTE_EXP(exp_time));
		ret |= ov_camera_module_write_reg(cam_mod,
			OV8825_AEC_PK_LONG_EXPO_2ND_REG,
			OV8825_FETCH_2ND_BYTE_EXP(exp_time));
		ret |= ov_camera_module_write_reg(cam_mod,
			OV8825_AEC_PK_LONG_EXPO_1ST_REG,
			OV8825_FETCH_1ST_BYTE_EXP(exp_time));
		if (cam_mod->state == OV_CAMERA_MODULE_STREAMING) {
			ret = ov_camera_module_write_reg(cam_mod,
				OV8825_AEC_GROUP_UPDATE_ADDRESS,
				OV8825_AEC_GROUP_UPDATE_END_DATA);
			ret = ov_camera_module_write_reg(cam_mod,
				OV8825_AEC_GROUP_UPDATE_ADDRESS,
				OV8825_AEC_GROUP_UPDATE_END_LAUNCH);
		}
	}

	if (IS_ERR_VALUE(ret))
		ov_camera_module_pr_err(cam_mod,
			"failed with error (%d)\n", ret);
	return ret;
}


